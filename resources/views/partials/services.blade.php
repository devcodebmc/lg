@push('js')
<script>
    document.addEventListener('DOMContentLoaded', function () {
        const modalTitle = document.querySelector('.modal-header .service-title');
        const modalSubtitle = document.querySelector('.modal-header .service-subtitle');
        const modalDescription = document.querySelector('.modal-section:nth-of-type(1) p');
        const modalFeatures = document.querySelector('.modal-section:nth-of-type(2) ul');

        // Captura el clic en cada overlay de servicio
        document.querySelectorAll('.service-overlay').forEach(overlay => {
            overlay.addEventListener('click', function () {
                // Obtiene los datos del servicio seleccionado
                const title = this.getAttribute('data-title');
                const subtitle = this.getAttribute('data-subtitle');
                const description = this.getAttribute('data-description');
                const features = this.getAttribute('data-features');

                // Actualiza el contenido del modal
                modalTitle.textContent = title;
                modalSubtitle.textContent = subtitle;
                modalDescription.innerHTML = description;
                modalFeatures.innerHTML = features;
            });
        });
    });
</script>
@endpush
